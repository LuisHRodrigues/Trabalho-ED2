public class Item {

    // Atributo que armazena o valor ligado ao objeto Item
    private int valor;

    // Construtor que inicializa o atributo 'valor' com o valor passado como
    // argumento
    public Item(int valor) {
        this.valor = valor;
    }

    // Metodos getter e setters
    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

}
